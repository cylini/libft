#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t siz)
{
	size_t	i;
	size_t	dst_len;
	size_t	src_len;

	i = 0;
	dst_len = 0;
	src_len = 0;
	while (dst[dst_len] != '\0' && dst_len < siz)
		dst_len++;
	while (src[src_len] != '\0')
		src_len++;
	if (siz <= dst_len)
		return (src_len + siz);
	while ((dst_len + i < siz - 1) && src[i])
	{
		dst[dst_len + i] = src[i];
		i++;
	}
	dst[dst_len + i] = '\0';
	return (src_len + dst_len);
}
